package com.example.myapplication;

public class Lesson {
    public String time, title, teacher, room, type;
    public int color;

    public Lesson(String time, String title, String teacher, String room, String type, int color) {
        this.time = time;
        this.title = title;
        this.teacher = teacher;
        this.room = room;
        this.type = type;
        this.color = color;
    }
}